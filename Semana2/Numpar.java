package Semana2;

public class Numpar {
    public static void main(String[] args) {
        // Imprimir numeros pares de 1-100
        for (int i = 1; i <= 100; i++) 
        {
            if(i%2==0)
            {
                // System.out.println(i);
                System.out.print(i + " - ");
            }
        }
    }
}
