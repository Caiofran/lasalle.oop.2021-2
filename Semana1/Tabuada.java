public class Tabuada {
    public static void main(String[] args) {

        int tabuada = 10;
        System.out.print("\nTabuada de 10:\n");
        for(int x = 0; x <= 10; x++){
            System.out.print(x + " x " + tabuada + " = ");
            System.out.println(x * tabuada);
        }
    }
}
